        echo "- Failed stage: \`$stage\`"
        echo "- Exit status: \`$exit_status\`"
        echo "- Measurements: unavailable; this failed run replaced the previous report so stale results cannot be mistaken for current evidence."
    } > "$report_tmp"
    mv "$report_tmp" "$BENCHMARK_MARKDOWN"
}

cleanup() {
    local exit_status=$?
    trap - EXIT
    set +e
    if [[ "$report_written" -ne 1 ]]; then
        write_failure_markdown "$exit_status"
    fi
    for implementation in sx c; do
        for corpus in repeat random; do
            for level in 1 6 9; do
                rm -f "$SCRATCH_ROOT/$implementation-$corpus-$level.zlib"
            done
        done
    done
    rm -f "$SCRATCH_ROOT/sx-exact.matrix" "$SCRATCH_ROOT/c-exact.matrix"
    rm -rf "$RUN_DIR"
    if [[ "$lock_acquired" -eq 1 ]]; then rmdir "$LOCK_DIR"; fi
    exit "$exit_status"
}
