write_failure_markdown() {
    local exit_status=$1
    local report_tmp="${BENCHMARK_MARKDOWN}.tmp.$$"
    {
        echo "# Latest std.compress benchmark"
        echo
        echo "> Generated by \`tests/bench_compress.sh\` on $started_utc. Do not edit measured values by hand."
        echo
