chmod +x "$FROZEN_SX_BIN"
sx_frozen_hash_before=$(sha256_file "$FROZEN_SX_BIN")
sx_source_hash_after_copy=$(sha256_file "$SX_SOURCE_BIN")
if [[ "$sx_source_hash_before" != "$sx_source_hash_after_copy" ||
      "$sx_source_hash_before" != "$sx_frozen_hash_before" ]]; then
    echo "SX compiler changed while its benchmark snapshot was being created" >&2
    echo "source before:     $sx_source_hash_before" >&2
    echo "source after copy: $sx_source_hash_after_copy" >&2
    echo "frozen copy:       $sx_frozen_hash_before" >&2
    exit 2
fi
sx_git_head=$(git rev-parse HEAD 2>/dev/null || printf unavailable)
if git status --short -- src build.zig build.zig.zon > "$SX_COMPILER_STATUS_LOG" 2>/dev/null; then
    sx_compiler_dirty_count=$(awk 'NF { count += 1 } END { print count + 0 }' "$SX_COMPILER_STATUS_LOG")
    if [[ "$sx_compiler_dirty_count" -eq 0 ]]; then
        sx_compiler_source_state="clean"
    else
        sx_compiler_source_state="dirty ($sx_compiler_dirty_count paths with uncommitted changes)"
    fi
else
    : > "$SX_COMPILER_STATUS_LOG"
    sx_compiler_dirty_count=0
    sx_compiler_source_state="unavailable"
fi

verify_miniz_file() {
    local name=$1
    local expected=$2
    local path="$MINIZ_SRC/$name"
    local actual
    if [[ ! -f "$path" ]]; then
        echo "pinned miniz source is missing $path" >&2
        return 1
    fi
    actual=$(sha256_file "$path")
    if [[ "$actual" != "$expected" ]]; then
        echo "pinned miniz hash mismatch for $name" >&2
        echo "expected: $expected" >&2
        echo "actual:   $actual" >&2
        return 1
    fi
}

stage="verifying pinned miniz $MINIZ_COMMIT"
