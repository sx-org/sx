metric() {
    local log=$1 corpus=$2 level=$3 key=$4
    awk -v corpus="$corpus" -v level="$level" -v key="$key" '
        $1 == corpus {
            matched_level = 0
            for (i = 2; i <= NF; ++i) {
                split($i, pair, "=")
                if (pair[1] == "level" && pair[2] == level) matched_level = 1
            }
            if (matched_level) {
                for (i = 2; i <= NF; ++i) {
                    split($i, pair, "=")
                    if (pair[1] == key) { print pair[2]; exit }
                }
            }
        }
    ' "$log"
}

read_metric() {
    local value
    value=$(metric "$1" "$2" "$3" "$4")
    if ! [[ "$value" =~ ^[0-9]+$ ]]; then
        echo "missing or invalid metric: log=$1 corpus=$2 level=$3 key=$4" >&2
        return 1
    fi
    printf '%s' "$value"
}

median() {
    printf '%s\n' "$@" | sort -n | awk '{ values[NR] = $1 } END { print values[(NR + 1) / 2] }'
}

aggregate_logs() {
    local output=$1
    shift
    : > "$output"
    local first=$1 corpus level input packed ratio encode_ns decode_ns log
    local -a encode_values decode_values
    for corpus in repetitive incompressible; do
        for level in 1 6 9; do
            input=$(read_metric "$first" "$corpus" "$level" input)
            packed=$(read_metric "$first" "$corpus" "$level" packed)
            ratio=$(read_metric "$first" "$corpus" "$level" ratio_permille)
            encode_values=()
            decode_values=()
            for log in "$@"; do
                encode_values+=("$(read_metric "$log" "$corpus" "$level" encode_ns)")
                decode_values+=("$(read_metric "$log" "$corpus" "$level" decode_ns)")
            done
            encode_ns=$(median "${encode_values[@]}")
            decode_ns=$(median "${decode_values[@]}")
            printf '%s level=%s input=%s packed=%s ratio_permille=%s encode_ns=%s decode_ns=%s encode_KiB_s=%s decode_KiB_s=%s\n' \
                "$corpus" "$level" "$input" "$packed" "$ratio" "$encode_ns" "$decode_ns" \
                "$((input * 1000000000 / 1024 / (encode_ns > 0 ? encode_ns : 1)))" \
                "$((input * 1000000000 / 1024 / (decode_ns > 0 ? decode_ns : 1)))" >> "$output"
        done
    done
}

run_sx_bench() {
    local encode_bench=$1 decode_bench=$2 output=$3
    "$encode_bench" > "$output"
    "$decode_bench" >> "$output"
}

speedup() {
    awk -v c_ns="$1" -v sx_ns="$2" 'BEGIN {
        if (sx_ns <= 0 || c_ns <= 0) { print "n/a"; exit }
        printf "%.3fx", c_ns / sx_ns
    }'
}

baseline_change() {
    awk -v baseline_ns="$1" -v current_ns="$2" 'BEGIN {
        if (baseline_ns <= 0 || current_ns <= 0) { print "n/a"; exit }
        printf "%+.2f%%", 100 * (current_ns - baseline_ns) / baseline_ns
    }'
}

paired_baseline_change_ppm() {
    local corpus=$1 level=$2 key=$3 pair current_ns baseline_ns
    local -a changes=()
    for ((pair = 0; pair < ${#sx_trials[@]}; ++pair)); do
        current_ns=$(read_metric "${sx_trials[$pair]}" "$corpus" "$level" "$key")
        baseline_ns=$(read_metric "${baseline_trials[$pair]}" "$corpus" "$level" "$key")
        changes+=("$(((current_ns - baseline_ns) * 1000000 / baseline_ns))")
    done
    median "${changes[@]}"
}

paired_baseline_change() {
    awk -v change_ppm="$1" 'BEGIN { printf "%+.2f%%", change_ppm / 10000 }'
}

paired_baseline_delta_ns() {
    local corpus=$1 level=$2 key=$3 pair current_ns baseline_ns
    local -a deltas=()
    for ((pair = 0; pair < ${#sx_trials[@]}; ++pair)); do
        current_ns=$(read_metric "${sx_trials[$pair]}" "$corpus" "$level" "$key")
        baseline_ns=$(read_metric "${baseline_trials[$pair]}" "$corpus" "$level" "$key")
        deltas+=("$((current_ns - baseline_ns))")
    done
    median "${deltas[@]}"
}

paired_baseline_status() {
    local change_ppm=$1 delta_ns=$2
    if (( change_ppm <= 0 || delta_ns <= 0 )); then
        printf 'PASS'
    elif (( change_ppm <= BASELINE_NOISE_PERCENT * 10000 || delta_ns <= BASELINE_NOISE_NS )); then
        printf 'NOISE'
    else
        printf 'REGRESSION'
    fi
}

count_baseline_regressions() {
    local regressions=0 corpus level key change_ppm delta_ns
    for corpus in repetitive incompressible; do
        for level in 1 6 9; do
            for key in encode_ns decode_ns; do
                change_ppm=$(paired_baseline_change_ppm "$corpus" "$level" "$key")
                delta_ns=$(paired_baseline_delta_ns "$corpus" "$level" "$key")
                if (( change_ppm > BASELINE_NOISE_PERCENT * 10000 && delta_ns > BASELINE_NOISE_NS )); then
                    regressions=$((regressions + 1))
                fi
            done
        done
    done
    printf '%s' "$regressions"
}

paired_total_delta_ns() {
    local pair corpus level key current_ns baseline_ns total_delta
    local -a deltas=()
    for ((pair = 0; pair < ${#sx_trials[@]}; ++pair)); do
        total_delta=0
        for corpus in repetitive incompressible; do
            for level in 1 6 9; do
                for key in encode_ns decode_ns; do
                    current_ns=$(read_metric "${sx_trials[$pair]}" "$corpus" "$level" "$key")
                    baseline_ns=$(read_metric "${baseline_trials[$pair]}" "$corpus" "$level" "$key")
                    total_delta=$((total_delta + current_ns - baseline_ns))
                done
            done
        done
        deltas+=("$total_delta")
    done
    median "${deltas[@]}"
}

paired_total_change_ppm() {
    local pair corpus level key current_ns baseline_ns total_current total_baseline
    local -a changes=()
    for ((pair = 0; pair < ${#sx_trials[@]}; ++pair)); do
        total_current=0
        total_baseline=0
        for corpus in repetitive incompressible; do
            for level in 1 6 9; do
                for key in encode_ns decode_ns; do
                    current_ns=$(read_metric "${sx_trials[$pair]}" "$corpus" "$level" "$key")
                    baseline_ns=$(read_metric "${baseline_trials[$pair]}" "$corpus" "$level" "$key")
                    total_current=$((total_current + current_ns))
                    total_baseline=$((total_baseline + baseline_ns))
                done
            done
        done
        changes+=("$(((total_current - total_baseline) * 1000000 / total_baseline))")
    done
    median "${changes[@]}"
}

paired_gross_delta_ns() {
    local direction=$1 corpus level key delta total=0
    for corpus in repetitive incompressible; do
        for level in 1 6 9; do
            for key in encode_ns decode_ns; do
                delta=$(paired_baseline_delta_ns "$corpus" "$level" "$key")
                if [[ "$direction" == gain && "$delta" -lt 0 ]]; then
                    total=$((total - delta))
                elif [[ "$direction" == loss && "$delta" -gt 0 ]]; then
                    total=$((total + delta))
                fi
            done
        done
    done
    printf '%s' "$total"
}

baseline_gate_status() {
    local regressions=$1 total_change_ppm=$2 total_delta_ns=$3
    if (( regressions == 0 )); then
        printf 'PASS'
    elif (( total_change_ppm < -BASELINE_NOISE_PERCENT * 10000 && total_delta_ns < -BASELINE_NOISE_NS )); then
        printf 'PASS'
    else
        printf 'FAIL'
    fi
}

policy_assert() {
    local label=$1 actual=$2 expected=$3
    if [[ "$actual" != "$expected" ]]; then
        echo "regression-policy self-test failed: $label: expected $expected, got $actual" >&2
        return 1
    fi
}

stage="self-testing regression policy boundaries"
policy_assert "faster row" "$(paired_baseline_status -1 -1)" PASS
policy_assert "absolute boundary is noise" "$(paired_baseline_status 20000 10000)" NOISE
policy_assert "percentage boundary is noise" "$(paired_baseline_status 10000 20000)" NOISE
policy_assert "both boundaries exceeded" "$(paired_baseline_status 10001 10001)" REGRESSION
policy_assert "no significant rows" "$(baseline_gate_status 0 1 1)" PASS
policy_assert "large aggregate gain offsets a regression" "$(baseline_gate_status 1 -10001 -10001)" PASS
policy_assert "percentage gain boundary does not offset" "$(baseline_gate_status 1 -10000 -10001)" FAIL
policy_assert "absolute gain boundary does not offset" "$(baseline_gate_status 1 -10001 -10000)" FAIL

